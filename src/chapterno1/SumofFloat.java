package chapterno1;

public class SumofFloat {

	public static void main(String[] args) {
		// Declare two float variables
        float num1 = 5.5f;
        float num2 = 10.3f;

        // Add the two numbers
        float sum = num1 + num2;

        // Print the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);

	}

}
