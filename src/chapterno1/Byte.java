package chapterno1;

public class Byte {

	public static void main(String[] args) {
		 byte a = 10;
	        byte b = 20;
	        byte sum = (byte) (a + b); // Casting required to avoid overflow

	        System.out.println("Value of a: " + a);
	        System.out.println("Value of b: " + b);
	        System.out.println("Sum of a and b: " + sum);

	}

}
