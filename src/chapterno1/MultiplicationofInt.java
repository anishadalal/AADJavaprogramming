package chapterno1;

public class MultiplicationofInt {

	public static void main(String[] args) {
		// Declare two integer variables
        int num1 = 7;
        int num2 = 5;

        // Multiply the two numbers
        int product = num1 * num2;

        // Print the result
        System.out.println("The product of " + num1 + " and " + num2 + " is: " + product);

	}

}
