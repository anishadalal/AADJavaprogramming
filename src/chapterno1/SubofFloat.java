package chapterno1;

public class SubofFloat {

	public static void main(String[] args) {
		// Declare two float variables
        float num1 = 15.5f;
        float num2 = 10.3f;

        // Subtraction the two numbers
        float sub = num1 - num2;

        // Print the result
        System.out.println("The subtraction of " + num1 + " and " + num2 + " is: " + sub);

	}

}
