package chapterno1;

public class SumofInt {

	public static void main(String[] args) {
		// Declare two integer variables
        int num1 = 5;
        int num2 = 10;

        // Add the two numbers
        int sum = num1 + num2;

        // Print the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);

	}

}
