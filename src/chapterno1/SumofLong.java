package chapterno1;

public class SumofLong {

	public static void main(String[] args) {
		// Declare two long variables
        long num1 = 10000000000L; // Notice the 'L' to indicate long literal
        long num2 = 20000000000L;

        // Add the two numbers
        long sum = num1 + num2;

        // Print the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);

	}

}
