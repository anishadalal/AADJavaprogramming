public class Long {

	public static void main(String[] args) {
		long num1 = 100000L;
        long num2 = 50000L;
        long product = num1 * num2;

        System.out.println("The first number is: " + num1);
        System.out.println("The second number is: " + num2);
        System.out.println("The product is: " + product);

	}

}
