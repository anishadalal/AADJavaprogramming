package chapterno1;

public class RemainderofInt {

	public static void main(String[] args) {
		int num1 = 10;  // First integer
        int num2 = 3;   // Second integer

        int remainder = num1 % num2;  // Calculate remainder

        System.out.println("Remainder of " + num1 + " divided by " + num2 + " is: " + remainder);

	}

}
