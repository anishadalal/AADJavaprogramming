package chapterno1;

public class SubofLong {

	public static void main(String[] args) {
		// Declare two long variables
        long num1 = 80000000000L; // Notice the 'L' to indicate long literal
        long num2 = 20000000000L;
		
     // Subtraction the two numbers
        float sub = num1 - num2;
		
     // Print the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sub);


	}

}
